            if (!string.IsNullOrEmpty(ds.PromptDirectoryPath))
                args.Add($"--prompt-dir {EscapePs(ds.PromptDirectoryPath)}");
            if (!string.IsNullOrEmpty(ds.ExpectedOutputDirectoryPath))
                args.Add($"--expected-dir {EscapePs(ds.ExpectedOutputDirectoryPath)}");
        }

        // Judge options
        if (config.Judge is { } judge)
        {
            if (!string.IsNullOrEmpty(judge.BaseUrl))
                args.Add($"--judge-url {EscapePs(judge.BaseUrl)}");
            if (judge.ServerConfig.Model is { Kind: ModelSourceKind.LocalFile } judgeModel && !string.IsNullOrEmpty(judgeModel.FilePath))
                args.Add($"--judge-model-file {EscapePs(judgeModel.FilePath)}");
            else if (judge.ServerConfig.Model is { Kind: ModelSourceKind.HuggingFace } judgeHf && !string.IsNullOrEmpty(judgeHf.HfRepo))
                args.Add($"--judge-hf-repo {EscapePs(judgeHf.HfRepo)}");
            if (!string.IsNullOrEmpty(judge.ServerConfig.ApiKey))
                args.Add($"--judge-api-key {EscapePs(judge.ServerConfig.ApiKey)}");
            if (!string.IsNullOrEmpty(judge.JudgePromptTemplate) && judge.JudgePromptTemplate != "standard")
                args.Add($"--judge-template {EscapePs(judge.JudgePromptTemplate)}");
            if (judge.ScoreMinValue != 0)
                args.Add($"--judge-score-min {judge.ScoreMinValue}");
            if (judge.ScoreMaxValue != 10)
                args.Add($"--judge-score-max {judge.ScoreMaxValue}");
        }

        // Output options
        if (!string.IsNullOrEmpty(config.Run.OutputDirectoryPath) && config.Run.OutputDirectoryPath != "./results")
            args.Add($"--output-dir {EscapePs(config.Run.OutputDirectoryPath)}");
        if (!string.IsNullOrEmpty(config.Run.RunName))
            args.Add($"--run-name {EscapePs(config.Run.RunName)}");

        // Run control
        if (config.Run.MaxConcurrentEvals.HasValue)
            args.Add($"--max-concurrent {config.Run.MaxConcurrentEvals}");
        if (!config.Run.ContinueOnEvalFailure)
            args.Add("--stop-on-failure");

        // Build the command
        _ = sb.AppendLine("# Run Seevalocal with all settings");

        // PowerShell uses backtick (`) for line continuation, not backslash (\)
        sb.Append("seevalocal");
        if (args.Count > 0)
        {
            _ = sb.AppendLine(" `");
            for (int i = 0; i < args.Count; i++)
            {
                _ = sb.Append($"  {args[i]}");
                // Only add continuation character if this is not the last argument
                if (i < args.Count - 1)
                {
                    _ = sb.AppendLine(" `");
                }
                else
                {
                    _ = sb.AppendLine();
                }
            }
        }
        else
        {
            _ = sb.AppendLine();
        }

        return sb.ToString();
    }

    // -------------------------------------------------------------------------
    // Escaping helpers
    // -------------------------------------------------------------------------

    private static string EscapeBash(string s)
    {
        if (s.Contains(' ') || s.Contains('\'') || s.Contains('"'))
            return $"'{s.Replace("'", "'\\''")}'";
        return s;
    }

    private static string EscapePs(string s)
    {
        if (s.Contains(' ') || s.Contains('"'))
            return $"\"{s.Replace("\"", "\"\"")}\"";
        return s;
    }
}
