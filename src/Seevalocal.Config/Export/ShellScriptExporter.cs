        if (!string.IsNullOrEmpty(llama.ChatTemplate))
            args.Add($"--chat-template {EscapePs(llama.ChatTemplate)}");
        if (!string.IsNullOrEmpty(llama.ReasoningFormat))
            args.Add($"--reasoning-format {EscapePs(llama.ReasoningFormat)}");
        if (llama.LogVerbosity.HasValue)
